    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

    // Retrieve input from the search text field
    String studentIdText = TFieldSearch.getText().trim();

    // Validate the input
    if (!LangCenValid.isValidStudentId(studentIdText)) {
        JOptionPane.showMessageDialog(this, "Invalid Student ID. Please enter a valid 4-digit ID.", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int studentId = Integer.parseInt(studentIdText);

    // Ensure the student list is sorted
    studentList.sort((a, b) -> Integer.compare(a.getStudentId(), b.getStudentId()));

    // Use the controller to perform binary search
    LangCenController controller = new LangCenController();
    LangCenModel studentFound = controller.searchStudentById(studentList, studentId);

    if (studentFound != null) {
        // Update the JTable with the found student
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
        model.setRowCount(0); // Clear the table
        model.addRow(new Object[]{
            studentFound.getStudentId(),
            studentFound.getStudent(),
            studentFound.getEmail(),
            studentFound.getPhone(),
            studentFound.getCourse()
        });

        JOptionPane.showMessageDialog(this, "Student found and displayed!", "Search Complete", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "Student ID not found.", "Search Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButton5ActionPerformed

