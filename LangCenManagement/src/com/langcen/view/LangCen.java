    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
    // Get the selected course from jComboBox1
    String selectedCourse = jComboBox1.getSelectedItem().toString();

    LangCenController controller = new LangCenController();
    studentList = controller.mergeSortByCourse(studentList);

    // If the selected course is "Alphabetical", show all courses in alphabetical order
    if ("Alphabetical".equalsIgnoreCase(selectedCourse)) {
        DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
        model.setRowCount(0); // Clear the table

        for (LangCenModel student : studentList) {
            model.addRow(new Object[]{
                student.getStudentId(),
                student.getStudent(),
                student.getEmail(),
                student.getPhone(),
                student.getCourse()
            });
        }

        JOptionPane.showMessageDialog(this, "Table sorted by course alphabetically!", "Operation Complete", JOptionPane.INFORMATION_MESSAGE);
        return;
    }

    // Otherwise, filter the sorted list to include only the selected course
    List<LangCenModel> filteredList = new ArrayList<>();
    for (LangCenModel student : studentList) {
        if (student.getCourse().equalsIgnoreCase(selectedCourse)) {
            filteredList.add(student);
        }
    }

    // Update the JTable with the filtered and sorted data
    DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
    model.setRowCount(0); // Clear the table

    for (LangCenModel student : filteredList) {
        model.addRow(new Object[]{
            student.getStudentId(),
            student.getStudent(),
            student.getEmail(),
            student.getPhone(),
            student.getCourse()
        });
    }

    // Provide feedback
    JOptionPane.showMessageDialog(this, "Table sorted by course and filtered for: " + selectedCourse, "Operation Complete", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton8ActionPerformed

